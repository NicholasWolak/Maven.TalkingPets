package io.zipcoder.pets;

public class Pets {
    String name;
    String speak;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSpeak() {
        return speak;
    }

    public void setSpeak(String speak) {
        this.speak = speak;
    }

    public String speak () {
        return speak;
    }

}
